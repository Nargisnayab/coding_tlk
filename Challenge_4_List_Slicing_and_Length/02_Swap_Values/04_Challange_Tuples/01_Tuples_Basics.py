# Creating a  tuples with different date type
my_tuple = (42, 3.14, 'Python', True, (1, 2, 3))
# Printing the entire tuple
print("Tuple:" , my_tuple)
# Accessing the third element
print("The third element:" ,my_tuple[2])
# Extracting and printing the first element of the nested tuple
print("The first element of the nested tuple: " , my_tuple [4][0] )
